namespace Webkul\MpServiceFee\Block\Adminhtml\Service\Button;

class Back extends \Magento\Catalog\Block\Adminhtml\Product\Edit\Button\Generic
{
    /**
     * Get button data
     *
     * @return array
     */
    public function getButtonData()
    {
        return [
            'label' => __('Back'),
            'on_click' => sprintf("location.href = '%s';", $this->getUrl('*/*/attributeslist')),
            'class' => 'back'
        ];
    }
}
